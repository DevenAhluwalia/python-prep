class TvControl:

	@staticmethod
	def on() -> None:

		print("Turning Tv ON")


	@staticmethod
	def off() -> None:

		print("Turning Tv OFF")


	@staticmethod
	def increase_channel_number() -> None:

		print("Channel number Increase")


	@staticmethod
	def decrease_channel_number() -> None:

		print("Channel number Decrease")
